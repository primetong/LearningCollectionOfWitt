package com.evguard.model;

public class AddressInfo {

	private String mAddress;
	private String ID;
	
	public AddressInfo() {
	}

	public String getAddress() {
		return mAddress;
	}

	public void setAddress(String mAddress) {
		this.mAddress = mAddress;
	}

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}
	
	
}
